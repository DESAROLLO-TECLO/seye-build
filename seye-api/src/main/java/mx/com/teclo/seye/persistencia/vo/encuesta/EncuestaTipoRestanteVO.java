package mx.com.teclo.seye.persistencia.vo.encuesta;

public class EncuestaTipoRestanteVO {
	
	private Long idUsuarioEncuesta;
	private Integer tpRestante;
	private Integer stContinua;
	
	public Long getIdUsuarioEncuesta() {
		return idUsuarioEncuesta;
	}
	public void setIdUsuarioEncuesta(Long idUsuarioEncuesta) {
		this.idUsuarioEncuesta = idUsuarioEncuesta;
	}
	public Integer getTpRestante() {
		return tpRestante;
	}
	public void setTpRestante(Integer tpRestante) {
		this.tpRestante = tpRestante;
	}
	public Integer getStContinua() {
		return stContinua;
	}
	public void setStContinua(Integer stContinua) {
		this.stContinua = stContinua;
	}
}
